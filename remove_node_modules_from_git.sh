#!/bin/bash

echo "Stopping Git from tracking node_modules files..."

# Make the .gitignore rule take effect
git rm -r --cached .
git add .
git status | grep "node_modules" > /dev/null

if [ $? -eq 0 ]; then
  echo "Note: Some node_modules files are still showing in git status."
  echo "You may need to commit these changes to complete the cleanup."
else
  echo "Success! node_modules are no longer being tracked by Git."
fi

echo "Done. You can now commit these changes with:"
echo "git commit -m \"Remove node_modules from git tracking\""
